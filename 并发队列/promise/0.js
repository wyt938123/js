// 是的，在现代 JavaScript 中，如果你想让异步代码看起来像同步代码一样，直接将结果赋值给一个变量，await 是唯一的途径。

// 主要取决于你对“直接拿到”的定义：

// 1. 使用 await（同步写法）
// 它可以让你直接在当前作用域通过赋值语句拿到结果。前提是必须在 async 函数内部。

// 2. 使用 .then()（回调写法）
// 你无法通过 const val = promise.then(...) 拿到 res。这里的 val 永远是一个新的 Promise 对象。你只能在回调函数内部处理这个结果。

// 为什么不能“直接”拿到？
// 因为 JavaScript 是单线程的，异步操作（如 setTimeout）在后台运行。

// 如果你同步地去“拿”结果，此时异步任务还没完成，结果还没产生。
// await 的本质是暂停当前函数的执行，等待异步操作完成并“解包” Promise，然后才继续往下走。
// 总结
// 如果你希望代码流是：结果 = 异步执行()，那么必须用 await。
// 如果你不使用 await，结果就永远被“锁”在 Promise 容器里，只能通过 .then(res => { ... }) 在闭包内部访问